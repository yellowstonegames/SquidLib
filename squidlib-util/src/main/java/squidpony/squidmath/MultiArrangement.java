    private int insert(final K k) {
        return insertAt(k, order.size);
    }
    private int insertAt(final K k, final int at) {
        int pos;
        if (k == null) {
            if (containsNullKey) {
                value[n].add(at);
                order.add(n);
                return n;
            }
            containsNullKey = true;
            pos = n;
        } else {
            K curr;
            final K[] key = this.key;
            // The starting point.
            if ((curr = key[pos = (hasher.hash(k)) & mask]) != null) {
                if (hasher.areEqual(curr, k)) {
                    value[pos].add(at);
                    order.insert(at, pos);
                    return pos;
                }
                while ((curr = key[pos = (pos + 1) & mask]) != null)
                    if (hasher.areEqual(curr, k)) {
                        value[pos].add(at);
                        order.insert(at, pos);
                        return pos;
                    }
            }
        }
        key[pos] = k;
        if (size == 0) {
            first = last = pos;
        } else {
            last = pos;
        }
        order.insert(at, pos);
        value[pos] = new IntVLA(8);
        value[pos].add(at);
        if (size++ >= maxFill)
            rehash(arraySize(size + 1, f));
        return -1;
    }

    public int add(final K k) {
        final int pos = insert(k);
        if (pos < 0)
            return defRetValue;
        final IntVLA oldValue = value[pos];
        oldValue.add(order.size - 1);
        return order.size - 1;
    }

